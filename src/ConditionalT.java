public class ConditionalT {
    public static void main(String[] args) {

        int number = 3;
// switch (단일값)
        switch(number){
            case 1:
                System.out.println("1 입니다.");
                break;
                case 2:
                    System.out.println("2입니다.");
                    break;
                    default:
                        System.out.println("1도 2도 아닙니다.");
        }
    }
}
